package Labsession;
import java.util.Scanner;
public class NaturalNoSequence {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int i=1;
        for(;i<=n;){
            System.out.print(i+" ");
            i++;
        }
    }


}
